package pods

import (
	"fmt"
	"net"
	"time"

	"github.com/bitvector2/testgo/named"
	"github.com/bitvector2/testgo/utils"
	log "github.com/golang/glog"
	"k8s.io/apimachinery/pkg/fields"
	"k8s.io/apimachinery/pkg/util/runtime"
	"k8s.io/apimachinery/pkg/util/wait"
	"k8s.io/client-go/kubernetes"
	apiV1 "k8s.io/client-go/pkg/api/v1"
	"k8s.io/client-go/rest"
	"k8s.io/client-go/tools/cache"
	"k8s.io/client-go/tools/clientcmd"
	"k8s.io/client-go/util/workqueue"
)

type Controller struct {
	indexer   cache.Indexer
	queue     workqueue.RateLimitingInterface
	informer  cache.Controller
	aclList   named.AclList
	viewList  named.ViewList
	outputDir string
}

func New(kubeConfig string, masterURL string, aclList named.AclList, viewList named.ViewList, outputDir string) *Controller {
	// creates the connection
	var config *rest.Config
	var err error
	if kubeConfig != "" {
		config, err = clientcmd.BuildConfigFromFlags(masterURL, kubeConfig)
	} else {
		config, err = rest.InClusterConfig()
	}
	if err != nil {
		log.Warningln(err)
	}

	// creates the clientSet
	clientSet, err := kubernetes.NewForConfig(config)
	if err != nil {
		log.Warningln(err)
	}

	// create the pod watcher
	podListWatcher := cache.NewListWatchFromClient(clientSet.CoreV1().RESTClient(), "pods", apiV1.NamespaceAll, fields.Everything())

	// create the workqueue
	queue := workqueue.NewRateLimitingQueue(workqueue.DefaultControllerRateLimiter())

	// Bind the workqueue to a cache with the help of an informer. This way we make sure that
	// whenever the cache is updated, the pod key is added to the workqueue.
	// Note that when we finally process the item from the workqueue, we might see a newer version
	// of the Pod than the version which was responsible for triggering the update.
	indexer, informer := cache.NewIndexerInformer(podListWatcher, &apiV1.Pod{}, 0, cache.ResourceEventHandlerFuncs{
		AddFunc: func(obj interface{}) {
			key, err := cache.MetaNamespaceKeyFunc(obj)
			if err == nil {
				queue.Add(key)
			}
		},
		UpdateFunc: func(old interface{}, new interface{}) {
			key, err := cache.MetaNamespaceKeyFunc(new)
			if err == nil {
				queue.Add(key)
			}
		},
		DeleteFunc: func(obj interface{}) {
			// IndexerInformer uses a delta queue, therefore for deletes we have to use this
			// key function.
			key, err := cache.DeletionHandlingMetaNamespaceKeyFunc(obj)
			if err == nil {
				queue.Add(key)
			}
		},
	}, cache.Indexers{})

	return &Controller{
		informer:  informer,
		indexer:   indexer,
		queue:     queue,
		aclList:   aclList,
		viewList:  viewList,
		outputDir: outputDir,
	}
}

func (c *Controller) processNextItem() bool {
	// Wait until there is a new item in the working queue
	key, quit := c.queue.Get()
	if quit {
		return false
	}
	// Tell the queue that we are done with processing this key. This unblocks the key for other workers
	// This allows safe parallel processing because two pods with the same key are never processed in
	// parallel.
	defer c.queue.Done(key)

	// Invoke the method containing the business logic
	err := c.processPod(key.(string))
	// Handle the error if something went wrong during the execution of the business logic
	c.handleErr(err, key)

	time.Sleep(1 * time.Second)
	return true
}

// processPod is the business logic of the controller. In this controller it simply prints
// information about the pod to stdout. In case an error happened, it has to simply return the error.
// The retry logic should not be part of the business logic.
func (c *Controller) processPod(key string) error {
	obj, exists, err := c.indexer.GetByKey(key)

	if err != nil {
		log.Errorf("fetching object with key %s from store failed with %v\n", key, err)
		return err
	}

	if exists {
		podIp := obj.(*apiV1.Pod).Status.PodIP
		acedEnv := obj.(*apiV1.Pod).GetObjectMeta().GetAnnotations()["concur.com/aced-environment"]
		acedDns := obj.(*apiV1.Pod).GetObjectMeta().GetAnnotations()["concur.com/aced-nameserver"]

		if podIp != "" && acedEnv != "" && acedDns != "" {
			ip, subnet, err := net.ParseCIDR(podIp + "/32")
			utils.Check(err) // FIXME

			acl := named.NewAcl(acedEnv)
			acl.AddElement(*named.NewCidrAddress(ip, subnet.Mask))
			c.aclList.AddAcl(*acl)
			c.aclList.Save()

			rr := named.NewResourceRecord("@", -1, "IN", "SOA", fmt.Sprintf("%s. root.%s. ( 2 604800 86400 2419200 604800 )", acedEnv, acedEnv))
			zone := named.NewZone(acedEnv, c.outputDir)
			zone.AddResourceRecord(*rr)

			view := named.NewView(acedEnv, *acl)
			view.AddZone(*zone)
			c.viewList.AddView(*view)
			c.viewList.Save()
		}
	} else {
		log.Infof("pod %s does not exist\n", key)
	}

	return nil
}

// handleErr checks if an error happened and makes sure we will retry later.
func (c *Controller) handleErr(err error, key interface{}) {
	if err == nil {
		// Forget about the #AddRateLimited history of the key on every successful synchronization.
		// This ensures that future processing of updates for this key is not delayed because of
		// an outdated error history.
		c.queue.Forget(key)
		return
	}

	// This controller retries 5 times if something goes wrong. After that, it stops trying.
	if c.queue.NumRequeues(key) < 5 {
		log.Warningf("error syncing pod %v: %v\n", key, err)

		// Re-enqueue the key rate limited. Based on the rate limiter on the
		// queue and the re-enqueue history, the key will be processed later again.
		c.queue.AddRateLimited(key)
		return
	}

	c.queue.Forget(key)
	// Report to an external entity that, even after several retries, we could not successfully process this key
	runtime.HandleError(err)
	log.Errorf("dropping pod %q out of the queue: %v\n", key, err)
}

func (c *Controller) Run(threadiness int, stopCh chan struct{}) {
	defer runtime.HandleCrash()

	// Let the workers stop when we are done
	defer c.queue.ShutDown()
	log.Infoln("starting pods controller")

	go c.informer.Run(stopCh)

	// Wait for all involved caches to be synced, before processing items from the queue is started
	if !cache.WaitForCacheSync(stopCh, c.informer.HasSynced) {
		runtime.HandleError(fmt.Errorf("timed out waiting for caches to sync"))
		return
	}

	for i := 0; i < threadiness; i++ {
		go wait.Until(c.runWorker, time.Second, stopCh)
	}

	<-stopCh
	log.Infoln("stopping pods controller")
}

func (c *Controller) runWorker() {
	for c.processNextItem() {
	}
}
